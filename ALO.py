# Функция для подсчета чисел с определенными последними цифрами квадрата
def sum_of_number():
    # Инициализируем переменную для хранения суммы
    total_sum = 0
    
    # Цикл от 1 до введенного числа n включительно
    for i in range(1, n + 1):
        # Вычисляем квадрат текущего числа
        square = i ** 2
        
        # Проверяем, заканчивается ли квадрат на 2, 5 или 8
        # str(square)[-1] преобразует число в строку и берет последний символ
        if str(square)[-1] in ['2','5','8']:
            # Если условие выполняется, увеличиваем счетчик на 1
            total_sum += 1
            
    # Возвращаем итоговую сумму    
    return total_sum

# Получаем число n от пользователя
n = int(input())

# Вызываем функцию и выводим результат
# Примечание: нужно использовать sum_of_number(), а не sum_of_number
print(sum_of_number())